function openNav(){
    document.getElementById("mySidepanel").style.width = "300px";
    document.getElementById("map").style.marginLeft = "300px";
    document.getElementById("myFooter").style.marginLeft = "300px";
    document.getElementById("h1-header").style.marginLeft = "260px";
}
function closeNav(){
    document.getElementById("mySidepanel").style.width = "0px";
    document.getElementById("map").style.marginLeft ="0px";
    document.getElementById("myFooter").style.marginLeft ="0px";
    document.getElementById("h1-header").style.marginLeft = "30px";
}